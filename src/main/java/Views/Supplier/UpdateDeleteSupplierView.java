      public void form_update() {
         String StrSearchUpdateDeleteMedicine = SearchSupplierNameInput.getText();
        if (StrSearchUpdateDeleteMedicine.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Supplier Name");
        } else {
            try {
                DB_CONNECTION db = new DB_CONNECTION();
                String Query = "select * from supplier where name like ?";
                db.Pstmt = db.Con.prepareStatement(Query);
                db.Pstmt.setString(1, SearchSupplierNameInput.getText() + "%");
                db.Rst = db.Pstmt.executeQuery();

                if (db.Rst.isBeforeFirst()) {
                    while (db.Rst.next()) {
                        suppliernameinput.setText(db.Rst.getString(2));
                        supplieremailinput.setText(db.Rst.getString(3));
                        supplierphoneinput.setText(db.Rst.getString(4));  
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Supplier Not Found! Please Enter correct Supplier Name");
                }

            } catch (HeadlessException | SQLException e) {
            }
        }
    }

