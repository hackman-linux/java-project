    private void UpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateBtnActionPerformed
        // TODO add your handling code here:
        String sname = SearchSupplierNameInput.getText();
        
         String Name = suppliernameinput.getText(), Email = supplieremailinput.getText(), Phone = supplierphoneinput.getText();
        if (Name.trim().isEmpty() || Email.trim().isEmpty() || Phone.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Fill And Select All The Required Fields");
        } else {
            try {
                DB_CONNECTION db = new DB_CONNECTION();
                 String Query = "select * from supplier where name like ?";
                 db.Pstmt = db.Con.prepareStatement(Query);
                 db.Pstmt.setString(1, sname + "%");
                 db.Rst = db.Pstmt.executeQuery();
               if (db.Rst.next()) {
                System.out.println("User found!"); // Debugging output
                String uid = db.Rst.getString("sup_id");
               
                String InsertQuery = "update supplier set name=?, email=?, phone=? where sup_id=?";
                db.Pstmt = db.Con.prepareStatement(InsertQuery);
                    db.Pstmt.setString(1, Name);
                    db.Pstmt.setString(2, Email);
                    db.Pstmt.setString(3, Phone);
                    db.Pstmt.setString(4, uid);
                int i = db.Pstmt.executeUpdate();
                if (i > 0) {
                     suppliernameinput.setText("");
                     supplieremailinput.setText("");
                     supplierphoneinput.setText("");
                     SearchSupplierNameInput.setText("");
                    JOptionPane.showMessageDialog(this, "Supplier Successfully Updated");
                } else {
                    JOptionPane.showMessageDialog(this, "Error In Update Supplier");
                }
                 }
            } catch (HeadlessException | SQLException e) {
            }
        }
    }//GEN-LAST:event_UpdateBtnActionPerformed

