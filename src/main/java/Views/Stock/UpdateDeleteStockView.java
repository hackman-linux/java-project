        public void form_update() {
         String StrSearchUpdateDeleteMedicine = SearchStockNameInput.getText();
        if (StrSearchUpdateDeleteMedicine.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Product Name");
        } else {
            try {
                DB_CONNECTION db = new DB_CONNECTION();
                String Query = "select * from product where name like ?";
                db.Pstmt = db.Con.prepareStatement(Query);
                db.Pstmt.setString(1, SearchStockNameInput.getText() + "%");
                db.Rst = db.Pstmt.executeQuery();

                if (db.Rst.isBeforeFirst()) {
                    while (db.Rst.next()) {
                        stockinput.setText(db.Rst.getString(2));
                        categoryinput.setText(db.Rst.getString(3));
                        stockpriceinput.setText(db.Rst.getString(4));  
                        stockqtyinput.setText(db.Rst.getString(5));
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Product Not Found! Please Enter correct Product Name");
                }

            } catch (HeadlessException | SQLException e) {
            }
        }
    }

