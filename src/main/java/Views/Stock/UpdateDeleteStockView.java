    private void UpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateBtnActionPerformed
        // TODO add your handling code here:
        
        String sname = SearchStockNameInput.getText();
   
         String Name = stockinput.getText(), Category = categoryinput.getText(), Price = stockpriceinput.getText(), Qty = stockqtyinput.getText();
        if (Name.trim().isEmpty() || Price.trim().isEmpty() || Category.trim().isEmpty() || Qty.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Fill And Select All The Required Fields");
        } else {
            try {
                DB_CONNECTION db = new DB_CONNECTION();
                 String Query = "select * from product where name like ?";
                 db.Pstmt = db.Con.prepareStatement(Query);
                 db.Pstmt.setString(1, sname + "%");
                 db.Rst = db.Pstmt.executeQuery();
               if (db.Rst.next()) {
                System.out.println("User found!"); // Debugging output
                String uid = db.Rst.getString("prod_id");
     
                String InsertQuery = "update product set name=?, category=?, price=?, quantity=? where prod_id=?";
                db.Pstmt = db.Con.prepareStatement(InsertQuery);
                    db.Pstmt.setString(1, Name);
                    db.Pstmt.setString(2, Category);
                    db.Pstmt.setString(3, Price);
                    db.Pstmt.setString(4, Qty);
                    db.Pstmt.setString(5, uid);
                int i = db.Pstmt.executeUpdate();
                if (i > 0) {
                     stockinput.setText("");
                     categoryinput.setText("");
                     stockpriceinput.setText("");
                     stockqtyinput.setText("");
                     SearchStockNameInput.setText("");
                    JOptionPane.showMessageDialog(this, "Product Successfully Updated");
                } else {
                    JOptionPane.showMessageDialog(this, "Error In Update Product");
                }
                 }
            } catch (HeadlessException | SQLException e) {
            }
        }
    }//GEN-LAST:event_UpdateBtnActionPerformed

